def deck() -> 'generator':
    """Функция которая создаёт упорядоченную колоду карт"""
    suits = ('черви', 'бубны', 'пики', 'трефы')
    for suit in suits:
        for num in range(2, 15):
            # ИСПОЛЬЗОВАТЬ: в данном случае скобки можно опустить
            yield num, suit


cards = deck()


